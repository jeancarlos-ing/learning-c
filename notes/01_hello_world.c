/*
    hello.c - "Hello, World!" Example

    This is the classic first program in C. It demonstrates the basic structure of a C program and how to print a message to the screen.

    Key Points:
    - Includes the standard input/output library for printing.
    - Defines the main function, which is the entry point of every C program.
    - Uses printf to display "Hello, World!" followed by a newline.
    - Returns 0 to indicate successful execution.
*/

#include <stdio.h> // Include the standard input/output library

int main(void) // Main function: program execution starts here
{
    printf("Hello, World!\n"); // Print message to the screen
    return 0; // Return 0 to signal successful completion
}